li=["rock","paper","scissor"]
user_score = 0
com_score = 0
tie = 0

def process():
    global user_score, com_score, tie
    com_choice = random.choice(li)
    choice = input("Enter your Choice(rock/paper/scissor): ").lower()
    if choice in li:
        print(f"Computer choice: {com_choice}")
        print(l[com_choice])
        print(f"Your choice: {choice}")
        print(l[choice])
        if choice == com_choice:
            print("It is Tie")
            tie+=1
        elif((choice == "rock" and com_choice == "scissor") or 
        (choice == "paper" and com_choice == "rock") or 
        (choice == "scissor" and com_choice == "paper")):
            print("You Win!!")
            user_score+=1
        else:
            print("You Lose")
            com_score+=1
    else:
        print("Invalid Input")
        process()
        
def main():
    process()
    while True:
        print("Final Scores:")
        print(f"You: {user_score} | Computer: {com_score} | Ties: {tie}")
        print("Do you want to play again(yes/no):")
        decision = input().lower()
        if(decision == 'no'):
            print("Thanks for playing...")
            break
        elif(decision == "yes"):
            process()
        else:
            print("Invalid Input")


